#normally reverse an array 

def rev_arr(arr):
    start = 0 ;
    end = len(arr) - 1

    while (start < end):
        arr[start] , arr[end] = arr[end] , arr[start]
        start += 1
        end -=1

my_list = [1, 2, 3, 4 ,5 ]
rev_arr(my_list)
